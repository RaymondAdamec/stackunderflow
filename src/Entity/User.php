    //     // Redirect or return a response
    // }

    /**
     * @see PasswordAuthenticatedUserInterface
     */
    public function getPassword(): string
    {
        return $this->password;
    }

    public function setPassword(string $password): static
    {
        $this->password = $password;

        return $this;
    }

    /**
     * @see UserInterface
     */
    public function eraseCredentials(): void
    {
        // If you store any temporary, sensitive data on the user, clear it here
        // $this->plainPassword = null;
    }

    public function getFirstName(): ?string
    {
        return $this->firstName;
    }

    public function setFirstName(string $firstName): static
    {
        $this->firstName = $firstName;

        return $this;
    }

    public function getLastName(): ?string
    {
        return $this->lastName;
    }

    public function setLastName(string $lastName): static
    {
        $this->lastName = $lastName;

        return $this;
    }

    public function getPicture(): ?string
    {
        return $this->picture;
    }

    public function setPicture(?string $picture): static
    {
        $this->picture = $picture;

        return $this;
    }

    public function getGitHubProfile(): ?string
    {
        return $this->gitHubProfile;
    }

    public function setGitHubProfile(?string $gitHubProfile): static
    {
        $this->gitHubProfile = $gitHubProfile;

        return $this;
    }

    public function getStatus(): ?string
    {
        return $this->isBanned;
    }
    public function setStatus(bool $isBanned): self
    {
        $this->isBanned = $isBanned;
        return $this;
    }

    public function getTimedBan(): ?\DateTimeInterface
    {
        return $this->TimedBan;
    }

    public function setTimedBan(?\DateTimeInterface $TimedBan): static
    {
        $this->TimedBan = $TimedBan;

        return $this;
    }
}
